def build_meta(tickers):
    """Build the metadata dict for all tickers."""
    meta = {}
    for t in tickers:
        # Check if it's a known ETF
        if t in ETFS:
            name, etf_sector = ETFS[t]
            meta[t] = {
                "name": name,
                "type": "ETF",
                "sector": etf_sector,
                "industry": "",
                "indices": []
            }
        # Check if it's a known stock
        elif t in STOCKS:
            name, sector, industry, indices = STOCKS[t]
            meta[t] = {
                "name": name,
                "type": "Stock",
                "sector": sector,
                "industry": industry,
                "indices": indices
            }
        else:
            # Unknown — mark as Other
            meta[t] = {
                "name": "",
                "type": "Stock",
                "sector": "Other",
                "industry": "",
                "indices": []
            }
    return meta


def main():
    with open(INPUT, "r") as f:
        tickers = json.load(f)

    print(f"Total tickers: {len(tickers)}")

    meta = build_meta(tickers)

    # Count classifications
    classified = sum(1 for v in meta.values() if v["sector"] != "Other")
    other_count = sum(1 for v in meta.values() if v["sector"] == "Other")
    etf_count = sum(1 for v in meta.values() if v["type"] == "ETF")
    stock_count = sum(1 for v in meta.values() if v["type"] == "Stock")

    print(f"Classified: {classified}")
    print(f"Other (unclassified): {other_count}")
    print(f"ETFs: {etf_count}")
    print(f"Stocks: {stock_count}")
    print()

    # Show which ones are unclassified
    others = sorted([t for t, v in meta.items() if v["sector"] == "Other"])
    print(f"Unclassified tickers ({len(others)}): {others}")

    with open(OUTPUT, "w") as f:
        json.dump(meta, f, indent=2)

    print(f"\nOutput written to {OUTPUT}")


